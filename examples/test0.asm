	ORG 0xDA00
BEGIN:		CALL CLS
			CALL HOME
			LXI H, VTIME
			CALL TIME
			LXI H, VDATE
			CALL DATE
			LXI H, VDAY
			CALL DAY
			LXI H, VDATE
			CALL DISPLAY
			LXI H, VGREET
			CALL DISPLAY
LOOP:			CALL CHGET
			CP A, 81 ; Q
			JP Z,THEEND
			CP A, 113 ; q
			JP Z,THEEND
			JP LOOP
			CP A, 80 ; P
			JP Z, PING
			CP A, 112 ; P
			JP Z, PING
			JP LOOP
PING:	LD A,1
			LXI H,CSRX
			LD M,A
			LXI H,CSRY
			LD A,5
			LD M,A
			LXI H,NPING
			CALL DISPLAY
			JP LOOP
THEEND:		CALL MENU
			END
VDATE:	DS "yy/mm/dd ("
VDAY:	DS "ddd) "
VTIME:	DS "hh:mm:ss "
	DB 13,10,0
VGREET:	DS "    LORA MESSENGER"
	DB 13,10
	DS "(P)ING      (F)REQUENCY (B)W        (S)F"
	DB 13,10
	DS "(Q)UIT"
	DB 13,10
EKEY:	DS "ENTER KEY:"
	DB 0
NPING: DS "PING!"
		DB 0
